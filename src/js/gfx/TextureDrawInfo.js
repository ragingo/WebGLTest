"use strict";
class TextureDrawInfo {
    get width() {
        return this._width;
    }
    set width(v) {
        this._width = v;
    }
    get height() {
        return this._height;
    }
    set height(v) {
        this._height = v;
    }
    get effectType() {
        return this._effectType;
    }
    set effectType(v) {
        this._effectType = v;
    }
    get color() {
        return this._color;
    }
    set color(v) {
        this._color = v;
    }
    get rotation() {
        return this._rotation;
    }
    set rotation(v) {
        this._rotation = v;
    }
    get scale() {
        return this._scale;
    }
    set scale(v) {
        this._scale = v;
    }
    get vivid() {
        return this._vivid;
    }
    set vivid(v) {
        this._vivid = v;
    }
    get polygonCount() {
        return this._polygonCount;
    }
    set polygonCount(v) {
        this._polygonCount = v;
    }
}
